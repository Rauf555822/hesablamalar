package main.java.ders14;

public class Printer {
    void print(String message) {
        System.out.println("Printing message:" + message);
    }

    void print(int number1) {
        System.out.println("Printing number:" + number1);
    }

    void print(double number2) {
        System.out.println("Printing double number:" + number2);
    }
}
